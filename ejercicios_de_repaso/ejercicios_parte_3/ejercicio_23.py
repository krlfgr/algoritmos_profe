'''Ejercicio 3.23
Suponga que un hostal tiene 15 camas para ofrecer
a turistas que visitan la zona. Las camas se ocupan
de manera individual. Construya un programa que
asigne un grupo de turistas-amigos que llegan al
hostal. Al hostal pueden llegar desde una persona
hasta un grupos de amigos de máximo 5 personas.
Si el cupo del hostal se llena no es posible atender
más turistas y el programa debe indicar esta
situación. Si todo el grupo que llega de amigos no se
puede ubicar en las camas disponibles del hostal no
se registrará ninguno de ellos y buscarán otro
hostal. Proponga un script que permita evaluar en
cada momento cuantas camas disponibles quedan y
si se ha llenado el hostal. Suponga que inicialmente
no hay turistas hospedados el día de hoy.'''